def get_text(language: str, key: str, **kwargs) -> str:
    """
    Get localized text for the given language and key.

    Args:
        language: Language code (e.g., 'en', 'ru', 'de')
        key: Text key to retrieve
        **kwargs: Optional format parameters (e.g., count=5)

    Returns:
        Localized text, with fallback to English if not found
    """
    # Get text from specified language, fallback to English
    texts = UI_TEXTS.get(language, UI_TEXTS['en'])
    text = texts.get(key, UI_TEXTS['en'].get(key, key))

    # Format with parameters if provided
    if kwargs:
        try:
            text = text.format(**kwargs)
        except KeyError:
            pass  # If formatting fails, return unformatted text

    return text
